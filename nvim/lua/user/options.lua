-- :help options
vim.opt.fileencoding = "utf-8"                        -- set the encoding of editor to be UTF-8
vim.opt.backspace = "indent,eol"                      -- prevent backspace from overdeleting
vim.opt.expandtab = true                              -- convert tab to spaces
vim.opt.tabstop = 4                                   -- tab insert 4 spaces
vim.opt.shiftwidth = 4                                -- ">>" insert 4 spaces
vim.opt.cursorline = true                             -- a horizontal line under cursor
vim.opt.number = true                                 -- enable line number on the left
vim.opt.termguicolors = true                          -- enable true colors
vim.opt.mouse = ""                                    -- disable mouse pointing function
vim.opt.wrap = false                                  -- disable line wrapping
vim.g.loaded_netrw = false                            -- disable netrw
vim.g.loaded_netrwPlugin = false                      -- disable netrw plugin
